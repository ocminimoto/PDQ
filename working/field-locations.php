<h3>Weather at the Field</h3>

       <p>&nbsp;We have a solar powered weather station at our outdoor flying site. Clicking on the "Weather" button on the main menu bar will take you to a Wunderground site that hosts the data from our station. The weather information includes:
         <ol>
            <li>Temperature</li>
            <li>Wind Speed and Direction</li>
            <li>Wind Direction</li>
            <li>Barometric Pressure</li>
            <li>Humidity</li>
            <li>Percipitation Intensity and Accumulated</li>
         </ol>
       <p>The staion updates the Wunderground site every 10 minutes. There is also a widget in the footer of each page of our website that gives the latest temperature and wind velocity.</p>

<h3>Field Appreciation Fridays</h3>

       <p>Every Friday from 10 AM to Noon is allocated to field maintenance, improvements, and general "sprucing up". Come during this period, and help to make our field a better place to be and bring your lunch so that you can stay to fly in the afternoon. </p>


